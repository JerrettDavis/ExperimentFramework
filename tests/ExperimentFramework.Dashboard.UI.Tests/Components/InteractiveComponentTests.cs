/// <summary>
/// bUnit tests that exercise async @code paths.
///
/// Pages without @rendermode InteractiveServer (Analytics, Targeting, HypothesisTesting)
/// call OnInitializedAsync unconditionally, so these tests exercise async state machines
/// simply by rendering the component.
///
/// Pages with @rendermode InteractiveServer (Experiments, Rollout) require
/// SetRendererInfo(isInteractive: true) to trigger data loading.
///
/// The async state-machine classes generated by the C# compiler for async @code methods
/// are instrumented when CompilerGeneratedAttribute is NOT in ExcludeByAttribute —
/// see tests/ExperimentFramework.Dashboard.UI.Tests/coverage.runsettings.
/// </summary>
public sealed class InteractiveComponentTests : BunitContext
{
