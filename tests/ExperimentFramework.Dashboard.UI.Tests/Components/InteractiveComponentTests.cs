    [Fact]
    public async Task Analytics_OnInitializedAsync_CallsGetUsageStats()
    {
        var called = false;
        var handler = new MultiEndpointHandler();
        handler.Add(UsageStatsUrl, _ =>
        {
            called = true;
            return JsonOk(new Dictionary<string, Dictionary<string, int>>
            {
                ["exp-1"] = new Dictionary<string, int> { ["control"] = 100, ["variant"] = 90 }
            });
        });
        handler.Add(AuditLogUrl, _ => JsonOk(new List<object>()));

        Services.AddSingleton(BuildApiClient(handler));

        Render<Analytics>();

        // The analytics page calls OnInitializedAsync which calls GetUsageStatsAsync synchronously
        // in bUnit's test renderer — we poll with small delay for async operations
        await Task.Delay(200);

        Assert.True(called, "GetUsageStatsAsync should be called during OnInitializedAsync");
    }

